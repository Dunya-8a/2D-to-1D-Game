function remove(x, y) {
	if (x >= 0 && y >= 0 && y < world.length && x < world[y].length) {
		world[y][x] = 0;
		return;
	}
}

function clear(p, dir) {
	switch (dir) {
		case 1: // up
			remove(p.x, p.y - 1);
			remove(p.x, p.y - 2);
			remove(p.x, p.y - 3);
			remove(p.x + 1, p.y - 1);
			remove(p.x + 1, p.y - 2);
			remove(p.x + 1, p.y - 3);
			p.y -= 3;
			break;
		case 2: // down
			remove(p.x, p.y + 1);
			remove(p.x, p.y + 2);
			remove(p.x, p.y + 3);
			remove(p.x, p.y + 4);
			remove(p.x + 1, p.y + 1);
			remove(p.x + 1, p.y + 2);
			remove(p.x + 1, p.y + 3);
			remove(p.x + 1, p.y + 4);
			p.y += 3;
			break;
		case 3: // left
			remove(p.x - 1, p.y);
			remove(p.x - 2, p.y);
			remove(p.x - 3, p.y);
			remove(p.x - 1, p.y + 1);
			remove(p.x - 2, p.y + 1);
			remove(p.x - 3, p.y + 1);
			p.x -= 3;
			break;
		case 4: // right
			remove(p.x + 1, p.y);
			remove(p.x + 2, p.y);
			remove(p.x + 3, p.y);
			remove(p.x + 4, p.y);
			remove(p.x + 1, p.y + 1);
			remove(p.x + 2, p.y + 1);
			remove(p.x + 3, p.y + 1);
			remove(p.x + 4, p.y + 1);
			p.x += 3;
			break;
		default:
			return;
	}
	++p.dist;
}

function available(x, y) {
	if (x >= 0 && y >= 0 && y < world.length && x < world[y].length) {
		return world[y][x] == 1;
	}
	return false;
}

function seedableRandom(seed)
{
	const x = Math.sin(seed++) * 10000;
	return x - Math.floor(x);
}

// generate a new world algorithmically
function generateWorld(size, seed) {
	size = size * 3 + 1;
	enemies = [];

	// fill a 2d array
	world = [];
	for (let i = 0; i < size; ++i) {
		world.push([]);
		for (let j = 0; j < size; ++j) {
			world[i].push(1);
		}
	}

	let pathways = [];

	pathways.push({ x: 1, y: 1, dist: 0 });
	remove(1, 1);
	remove(1, 2);
	remove(2, 1);

	let maxPathway = pathways[0];

	while (pathways.length > 0) {
		for (let i = 0; i < pathways.length; ++i) {
			let p = pathways[i];
			let directions = [];
			if (available(p.x, p.y - 3)) {
				directions.push(1); // up
			}
			if (available(p.x, p.y + 4)) {
				directions.push(2); // down
			}
			if (available(p.x - 3, p.y)) {
				directions.push(3); // left
			}
			if (available(p.x + 4, p.y)) {
				directions.push(4); // right
			}

			// console.log(seedableRandom(seed));
			// console.log(seed)

			for (let j = directions.length; j >= 0; --j) {
				if (Math.random() > 0.65) {
					// clone p for new branch
					let pathway = Object.assign({}, p);
					clear(pathway, directions[j]);
					pathways.push(pathway);
					directions.splice(j, 1);
				}
			}

			if (directions.length == 0) {
				if (p.dist > maxPathway.dist) {
					maxPathway = p;
				} else if (spawnEnemies && p.dist > 5 && enemies.length < enemyMax) {
					// spawn an enemy at the end of a pathway
					if (Math.random() > 0.85) {
						enemies.push({
							x: p.x + 0.5,
							y: p.y + 0.5,
							xVel: 0,
							yVel: 0,
							type: enemyType,
							dir: 0,
							state: 0,
							health: 100,
						});
					}
				}
				pathways.splice(i, 1);
			}
		}
	}

	// add the exit block
	world[maxPathway.y][maxPathway.x] = 2;
}
